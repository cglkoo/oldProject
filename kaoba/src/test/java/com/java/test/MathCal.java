package com.java.test;

public class MathCal {
	
	//加法；
	public int add(int n1, int n2){
		return n1+n2;
	}

	//减法；
	public int min(int n1, int n2){
		return n1-n2;
	}

	
	//乘法
	public int mul(int n1, int n2){
		return n1*n2;
	}

	
	//除法；
	public int div(int n1, int n2){
		return n1/n2;
	}
	
	public boolean compl(int n1, int n2){
		return n1>n2;
	}
	 
}
